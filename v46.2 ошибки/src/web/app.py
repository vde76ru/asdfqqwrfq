def register_websocket_handlers(socketio):
    """–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ WebSocket"""
    
    # –°–æ–∑–¥–∞–µ–º WebSocket –º–µ–Ω–µ–¥–∂–µ—Ä
    from .enhanced_websocket import EnhancedWebSocketManager, WebSocketBotIntegration
    ws_manager = EnhancedWebSocketManager()
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å bot_manager, —Å–æ–∑–¥–∞–µ–º –∏–Ω—Ç–µ–≥—Ä–∞—Ü–∏—é
    if bot_manager:
        ws_integration = WebSocketBotIntegration(ws_manager, bot_manager)
        
        # ‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–û: handle_connect —Ç–µ–ø–µ—Ä—å –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä auth
        @socketio.on('connect')
        def handle_connect(auth):
            logger.info("‚úÖ WebSocket –∫–ª–∏–µ–Ω—Ç –ø–æ–¥–∫–ª—é—á–µ–Ω")
            # –ó–∞–ø—É—Å–∫–∞–µ–º –∏–Ω—Ç–µ–≥—Ä–∞—Ü–∏—é –µ—Å–ª–∏ –µ—â–µ –Ω–µ –∑–∞–ø—É—â–µ–Ω–∞
            if not ws_integration.running:
                # ‚úÖ –ò–°–ü–†–ê–í–õ–ï–ù–û: —Å–æ–∑–¥–∞–µ–º task –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø–æ—Ç–æ–∫–µ
                import threading
                def run_async():
                    loop = asyncio.new_event_loop()
                    asyncio.set_event_loop(loop)
                    loop.run_until_complete(ws_integration.start())
                
                thread = threading.Thread(target=run_async, daemon=True)
                thread.start()
        
        @socketio.on('disconnect')
        def handle_disconnect():
            logger.info("‚ùå WebSocket –∫–ª–∏–µ–Ω—Ç –æ—Ç–∫–ª—é—á–µ–Ω")
        
        # –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∑–∞–ø—Ä–æ—Å–∞ —Å—Ç–∞—Ç—É—Å–∞
        @socketio.on('get_status')
        def handle_get_status():
            if bot_manager:
                status = bot_manager.get_status()
                emit('bot_status_update', status)
        
        # –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
        @socketio.on('bot_command')
        def handle_bot_command(data):
            command = data.get('command')
            
            if command == 'start':
                success, message = bot_manager.start()
                emit('command_response', {
                    'command': 'start',
                    'success': success,
                    'message': message
                })
            elif command == 'stop':
                success, message = bot_manager.stop()
                emit('command_response', {
                    'command': 'stop',
                    'success': success,
                    'message': message
                })
        
        # –ü–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∞—è –æ—Ç–ø—Ä–∞–≤–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π –≤—Å–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–Ω—ã–º –∫–ª–∏–µ–Ω—Ç–∞–º
        def broadcast_updates():
            while True:
                time.sleep(2)  # –ö–∞–∂–¥—ã–µ 2 —Å–µ–∫—É–Ω–¥—ã
                if bot_manager:
                    try:
                        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å—Ç–∞—Ç—É—Å
                        status = bot_manager.get_status()
                        socketio.emit('bot_status_update', status)
                        
                        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –±–∞–ª–∞–Ω—Å
                        balance = bot_manager.get_balance_info()
                        socketio.emit('balance_update', balance)
                        
                        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–∑–∏—Ü–∏–∏
                        positions = bot_manager.get_positions_info()
                        socketio.emit('positions_update', {
                            'positions': positions,
                            'count': len(positions)
                        })
                    except Exception as e:
                        logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π: {e}")
        
        # –ó–∞–ø—É—Å–∫–∞–µ–º —Ñ–æ–Ω–æ–≤—ã–π –ø–æ—Ç–æ–∫ –¥–ª—è broadcast
        broadcast_thread = threading.Thread(target=broadcast_updates, daemon=True)
        broadcast_thread.start()
        
        logger.info("‚úÖ WebSocket handlers –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω—ã —Å –∏–Ω—Ç–µ–≥—Ä–∞—Ü–∏–µ–π –±–æ—Ç–∞")
    else:
        logger.warning("‚ö†Ô∏è WebSocket handlers –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω—ã –±–µ–∑ –∏–Ω—Ç–µ–≥—Ä–∞—Ü–∏–∏ –±–æ—Ç–∞")

def register_app_handlers(app_instance: Flask):
    """–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ –æ—à–∏–±–æ–∫ –∏ –∑–∞–ø—Ä–æ—Å–æ–≤ —É—Ä–æ–≤–Ω—è –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è."""
    logger.info("... –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ –æ—à–∏–±–æ–∫ –∏ –∑–∞–ø—Ä–æ—Å–æ–≤ ...")

    @app_instance.errorhandler(404)
    def not_found_error(error):
        if request.path.startswith('/api/'):
            return jsonify({'success': False, 'error': 'Endpoint not found'}), 404
        return render_template('404.html'), 404

    @app_instance.errorhandler(500)
    def internal_error(error):
        logger.error(f"Internal Server Error on {request.path}: {error}", exc_info=True)
        if request.path.startswith('/api/'):
            return jsonify({'success': False, 'error': 'An internal server error occurred.'}), 500
        return render_template('500.html'), 500

    @app_instance.before_request
    def before_request():
        session.permanent = True

def log_registered_routes(app_instance: Flask):
    """–í—ã–≤–æ–¥–∏—Ç –≤ –ª–æ–≥ –≤—Å–µ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Ä–æ—É—Ç—ã –¥–ª—è –¥–∏–∞–≥–Ω–æ—Å—Ç–∏–∫–∏."""
    logger.info("üîç –î–∏–∞–≥–Ω–æ—Å—Ç–∏–∫–∞ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Ä–æ—É—Ç–æ–≤ –≤ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–∏:")
    rules = sorted(app_instance.url_map.iter_rules(), key=lambda r: r.rule)
    for rule in rules:
        methods = ','.join(sorted(rule.methods))
        logger.info(f"   - {rule.endpoint:30s} {methods:30s} -> {rule.rule}")

